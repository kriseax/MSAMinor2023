#For loop example
#print positive integers from 0 to 10
for number in range(11):
    print(number)

print("\n\n")
#print positive integers from 1 to 15
for x in range(1, 16):
    print(x)

print("\n\n")
#print positive even numbers between 0 and 20
for number in range(-10, 21, -2):
    print(number + 0.5)